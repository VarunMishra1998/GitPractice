a = int(input("enter a first number:"))
b = int(input("enter a second number:"))
c = input("enter the operation you want to perform (+, -, *, /):")
if c == "+":
    print(a + b)
elif c == "-":
    print(a - b)
elif c == "*":
    print(a * b)
elif c == "/":
    print(a / b)
#pull github to computer
else:
    print("invalid input")
    
